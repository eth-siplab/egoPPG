class RABlock(nn.Module):
    def __init__(self, input_size, w, h, kernel_size=3, stride=1, padding=1, bias=True):
        super(RABlock, self).__init__()
        self.conv3d = nn.Conv3d(input_size, input_size, kernel_size, stride, padding)
        self.gap = nn.AdaptiveAvgPool3d((1, h//2, w//2))  # used to be (32, 32), for ego (24, 64)
        self.conv2d_1 = nn.Conv2d(input_size, int(input_size/2), kernel_size, stride, padding, bias=bias)
        self.act1 = nn.ReLU(True)
        self.conv2d_2 = nn.Conv2d(int(input_size/2), 1, kernel_size, stride, padding, bias=bias)
        self.act2 = nn.Sigmoid()

    def forward(self, x):
        B,C,T,H,W = x.size()
        out = self.conv3d(x)
        feat=self.gap(out)
        feat=feat.squeeze(2)
        feat=self.conv2d_1(feat)
        feat=self.act1(feat)
        feat=self.conv2d_2(feat)
        feat=self.act2(feat)
        feat=feat.view(B, 1, 1, H, W)
        out=torch.mul(out,feat)
        out=out+x
        return out


class ConvBlock(nn.Module):
    def __init__(self, input_size, output_size, kernel_size=3, stride=1, padding=1, bias=True, activation='prelu',
                 norm=None):
        super(ConvBlock, self).__init__()
        self.conv = nn.Conv2d(input_size, output_size, kernel_size, stride, padding, bias=bias)

        self.norm = norm
        if self.norm == 'batch':
            self.bn = nn.BatchNorm2d(output_size)
        elif self.norm == 'instance':
            self.bn = nn.InstanceNorm2d(output_size)

        self.activation = activation
        if self.activation == 'relu':
            self.act = nn.ReLU(True)
        elif self.activation == 'prelu':
            self.act = nn.PReLU()
        elif self.activation == 'lrelu':
            self.act = nn.LeakyReLU(0.1, True)
        elif self.activation == 'tanh':
            self.act = nn.Tanh()
        elif self.activation == 'sigmoid':
            self.act = nn.Sigmoid()

    def forward(self, x):
        if self.norm is not None:
            out = self.bn(self.conv(x))
        else:
            out = self.conv(x)

        if self.activation is not None:
            return self.act(out)
        else:
            return out


